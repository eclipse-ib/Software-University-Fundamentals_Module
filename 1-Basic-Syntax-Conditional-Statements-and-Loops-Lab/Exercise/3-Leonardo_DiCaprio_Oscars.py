number = int(input())

leo_text = ""

if number == 86:
    leo_text = "Not even for Wolf of Wall Street?!"
elif 86 != number < 88:
    leo_text = "When will you give Leo an Oscar?"
elif number == 88:
    leo_text = "Leo finally won the Oscar! Leo is happy"
else:
    leo_text = "Leo got one already!"
print(leo_text)